# numbers 리스트에서 세 번째 요소가 30이면 "정답"을 출력하세요
numbers = [10, 20, 30, 40, 50]
if numbers[2] == 30:
    print('정답')

# fruits 리스트에서 첫 번째 요소가 "사과"이면 "Apple"을 출력하세요
fruits = ["사과", "바나나", "포도", "딸기"]
if fruits[0] == '사과':
    print('Apple')

# scores 리스트에서 마지막 요소가 90 이상이면 "합격",
# 그렇지 않으면 "불합격"을 출력하세요
scores = [70, 95, 60, 88]

if scores[-1] >= 90:
    print('합격')
else:
    print('불합격')